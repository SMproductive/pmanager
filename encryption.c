/* Function implementations */
static void
subBytes(uint8_t *blockMessage, uint16_t blockLen)
{
	for (uint16_t i=0;i<blockLen;i++) {
		blockMessage[i] = sBox[blockMessage[i]];
	}
}

static void
invSubBytes(uint8_t *blockMessage, uint16_t blockLen)
{
	for (uint16_t i=0;i<blockLen;i++) {
		blockMessage[i] = sBoxInv[blockMessage[i]];
	}
}

static void
addRoundKey(uint8_t *roundKey, uint8_t *key,
		uint16_t blockLen, uint16_t roundCount)
{
	/* Logic */
	for (uint16_t i=0;i<blockLen;i++) {
		roundKey[i] = key[roundCount] * key[blockLen + i];
	}
}

static void
roundKeyUse(uint8_t *blockMessage, uint8_t *roundKey, uint16_t blockLen)
{
	/* Logic */
	blockMessage[0] ^= roundKey[0];
	for (uint16_t i=1;i<blockLen;i++) {
		blockMessage[i] = blockMessage[i-1]^blockMessage[i]^roundKey[i];
	}
}

static void
revRoundKeyUse(uint8_t *blockMessage, uint8_t *roundKey, uint16_t blockLen)
{
	/* Logic */
	for (uint16_t i=blockLen-1;i>0;i--) {
		blockMessage[i] = blockMessage[i-1]^blockMessage[i]^roundKey[i];
	}
	blockMessage[0] ^= roundKey[0];
}

static void
mixBlock(uint8_t *blockMessage, uint16_t blockLen, uint16_t roundCount)
{
	/* Variables */
	uint8_t temp;
	uint16_t offset = roundCount + 1;

	/* Logic */
	for (uint16_t i=0;(i+offset)<blockLen;i++) {
		temp = blockMessage[i];
		blockMessage[i] = blockMessage[i+offset];
		blockMessage[i+offset] = temp;
		if((i+1)%offset==0) i+=(offset+1);
	}
}

void
messagePadding(struct messageArray **message, uint16_t keyLen)
{
	/* Variables */
	struct messageArray *expandedMessage;
	uint16_t blockLen;
	uint16_t paddingLen;

	/* Logic */
	expandedMessage = calloc(1, sizeof(*expandedMessage));
	blockLen = keyLen/2;
	if ((*message)->length%blockLen<=blockLen-2) {
		expandedMessage->length = (size_t)((*message)->length/blockLen + 1)
			* blockLen;
	} else {
		expandedMessage->length = (size_t)((*message)->length/blockLen + 2)
		       	* blockLen;
	}
	expandedMessage->array = malloc(
			expandedMessage->length*sizeof(
				expandedMessage->array[0]));
	paddingLen = expandedMessage->length - (*message)->length;
	for (size_t i=(*message)->length-1;i<(*message)->length;i--) {
		expandedMessage->array[i+paddingLen] = (*message)->array[i];
		(*message)->array[i] = 0;
	}
	free(*message);
	expandedMessage->array[0] = paddingLen%256;
	expandedMessage->array[1] = (paddingLen-expandedMessage->array[0])/256;
	*message = expandedMessage;
}

void
reverseMessagePadding(struct messageArray **expandedMessage, uint16_t keyLen)
{
	/* Variables */
	struct messageArray *message;
	uint16_t blockLen;
	uint16_t paddingLen;

	/* Logic */
	message = calloc(1, sizeof(*message));
	blockLen = keyLen/2;
	paddingLen = (*expandedMessage)->array[0]+(*expandedMessage)->array[1]*256;
	message->length = (*expandedMessage)->length - paddingLen;
	message->array = malloc(message->length*sizeof(message->array[0]));
	for (size_t i=message->length-1;i<message->length;i--) {
		message->array[i] = (*expandedMessage)->array[i+paddingLen];
		(*expandedMessage)->array[i+paddingLen] = 0;
	}
	free(*expandedMessage);
	*expandedMessage = message;
}

void
blockEnc(uint8_t *blockMessage, struct keyArray *key)
{
	/* Variables */
	uint8_t *roundKey;
	uint16_t roundCount;
	uint16_t blockLen;

	/* Logic */
	blockLen = key->length/2;
	roundKey = malloc(blockLen*sizeof(*roundKey));
	for (roundCount=0;roundCount<blockLen;roundCount++) {
		addRoundKey(roundKey, &key->array[0], blockLen, roundCount);
		subBytes(blockMessage, blockLen);
		roundKeyUse(blockMessage, roundKey, blockLen);
		mixBlock(blockMessage, blockLen, roundCount);
	}
	free(roundKey);
}

void
blockDec(uint8_t *blockMessage, struct keyArray *key)
{
	/* Variables */
	uint8_t *roundKey;
	uint16_t roundCount;
	uint16_t blockLen;

	/* Logic */
	blockLen = key->length/2;
	roundKey = malloc(blockLen*sizeof(*roundKey));
	for (roundCount=blockLen-1;roundCount<blockLen;roundCount--) {
		addRoundKey(roundKey, &key->array[0], blockLen, roundCount);
		mixBlock(blockMessage, blockLen, roundCount);
		revRoundKeyUse(blockMessage, roundKey, blockLen);
		invSubBytes(blockMessage, blockLen);
	}
	free(roundKey);
}

void
cbcEnc(struct messageArray **message, struct keyArray *key)
{
	/* Variables */
	size_t block;
	uint16_t blockLen;
	uint8_t *blockMessage;
	uint8_t *previousBlockMessage;

	/* Logic */
	blockLen = key->length/2;
	messagePadding(message, key->length);

	blockMessage = &(*message)->array[0];
	blockEnc(blockMessage, key);
	for (block=1;block<(*message)->length/blockLen;block++) {
		previousBlockMessage = blockMessage;
		blockMessage = &(*message)->array[block*blockLen];
		for (uint16_t i=0;i<blockLen;i++) {
			blockMessage[i] ^= previousBlockMessage[i];
		}
		blockEnc(blockMessage, key);
	}
}

void
cbcDec(struct messageArray **message, struct keyArray *key)
{
	/* Variables */
	size_t block;
	uint16_t blockLen;
	uint8_t *blockMessage;
	uint8_t *previousBlockMessage;

	/* Logic */
	blockLen = key->length/2;

	for (block=(*message)->length/blockLen-1;block>0;block--) {
		blockMessage = &(*message)->array[block*blockLen];
		previousBlockMessage = &(*message)->array[(block-1)*blockLen];
		blockDec(blockMessage, key);
		for (uint16_t i=0;i<blockLen;i++) {
			blockMessage[i] ^= previousBlockMessage[i];
		}
	}
	blockMessage = &(*message)->array[0];
	blockDec(blockMessage, key);
	reverseMessagePadding(message, key->length);
}
